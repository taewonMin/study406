package kr.or.ddit.quiz.vo;

public class SubjectVO {
	private String subNo;
	private String subParentCode;
	private String subParentName;
	private String subCode;
	private String subName;
	private String subDescript;
	
	
	public String getSubNo() {
		return subNo;
	}
	public void setSubNo(String subNo) {
		this.subNo = subNo;
	}
	public String getSubParentCode() {
		return subParentCode;
	}
	public void setSubParentCode(String subParentCode) {
		this.subParentCode = subParentCode;
	}
	public String getSubParentName() {
		return subParentName;
	}
	public void setSubParentName(String subParentName) {
		this.subParentName = subParentName;
	}
	public String getSubCode() {
		return subCode;
	}
	public void setSubCode(String subCode) {
		this.subCode = subCode;
	}
	public String getSubName() {
		return subName;
	}
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public String getSubDescript() {
		return subDescript;
	}
	public void setSubDescript(String subDescript) {
		this.subDescript = subDescript;
	}
	
	
	
}
